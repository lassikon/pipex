#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		if (str1[i] == '\0' || str2[i] == '\0')
			return (0);
		i++;
	}
	return (0);
}
/*
Lexicographically compares up to n characters from the null-terminated
strings s1 and s2. Returns > 0, 0, or < 0 depending on whether s1 > s2,
s1 == s2, or s1 < s2.
*/